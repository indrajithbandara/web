    private void reset()
    {
        okButton.setActionCommand("connect");
        resetButton.setEnabled(false);
        okButton.setEnabled(true);
        serverTextField.setText("");
        inputPanel.setVisible(false);
        resultLabel.setText("");
        resultLabel.setIcon(null);
        serverTextField.requestFocus();
        pack();
    }

